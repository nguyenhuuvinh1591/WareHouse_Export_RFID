    public void loadData(){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object[] obj = new Object[4];
        model.setRowCount(0);
        List<Product> prods = productdao.getProduct();
        int temp = prods.size()+1;
        jTextField1.setText("PD0" + temp);
        for(Product p:prods){
            obj[0] = p.getPid();
            obj[1] = p.getName();
            obj[2] = p.getQuantity();
            obj[3] = p.getPrice();
            model.addRow(obj);
        }
        model.fireTableDataChanged();
    }
    

