    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        List<Product> prods = new ArrayList<>();
        
    	for(int i=0;i<model.getRowCount();i++) {
    		Product p = new Product();
    		p.setPid((String)model.getValueAt(i, 0));
    		p.setName((String)model.getValueAt(i, 1));
    		p.setPrice((int)model.getValueAt(i, 3));
    		//String quan = (String)model.getValueAt(i, 2);
    		p.setQuantity(Integer.parseInt(model.getValueAt(i, 2).toString()));
    		prods.add(p);
    	}
    	productdao.updateProductList(prods);
    	loadData();
    }//GEN-LAST:event_jButton2ActionPerformed


